class A:
    def input(self,a,b):
        self.a=a
        self.b=b

    def display(self):
        print("The first value is = ",self.a)
        print("The second value is = ",self.b)
obj=A()#object of class A
obj.input(5,6)#object call
obj.display()#object call
































